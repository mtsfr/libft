#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	resul;
	int	i;
	int	sign;

	resul = 0;
	i = 0;
	sign = 1;
	while (nptr[i] == ' ' || (nptr[i] >= '\t' && nptr[i] <= '\r'))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{	
		if (nptr[i] == '-')
		{
			sign = -1;
		}
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		resul = resul * 10 + (nptr[i] - '0');
		i++;
	}
	return (resul * sign);
}
